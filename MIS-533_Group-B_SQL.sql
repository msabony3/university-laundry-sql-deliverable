ORDER BY student_name;
			/* compound conditions, order by */

-- Report 9: Most popular days of the week for machine usage --
SELECT DAYNAME(reservation_date), COUNT(*) 
FROM RESERVATION
GROUP BY DAYNAME(reservation_date)
ORDER BY DAYNAME(reservation_date) DESC;
			/* functions, group by, order by */

-- Report 10: Student/washer and student/dryer ratios --
SELECT B.building_id, (washer_count/dryer_count) AS 'washer/dryer_ratio', ROUND(student_count/washer_count) 
	   AS 'student/washer_ratio', ROUND(student_count/dryer_count) AS 'student/dryer_ratio', student_count, 
       COUNT(DISTINCT(time_id)) AS loads_done
FROM BUILDING B, MACHINE M, RESERVATION R
WHERE B.building_id = M.building_id AND M.machine_id = R.machine_id
GROUP BY building_id;
