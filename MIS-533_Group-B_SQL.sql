/* Creating and using database */
CREATE DATABASE FinalDeliverable;
USE FinalDeliverable;

/* Creating the six tables and assigning PKs and FKs */
CREATE TABLE building
(building_id CHAR(2) PRIMARY KEY,
building_name CHAR(12) NOT NULL,
washer_count INT(2) NOT NULL,
dryer_count INT(2) NOT NULL,
student_count INT(3) NOT NULL
);

CREATE TABLE student
(student_id CHAR(9) PRIMARY KEY,
student_name CHAR(30) NOT NULL,
student_email CHAR(24) NOT NULL,
building_id CHAR(2) NOT NULL,
CONSTRAINT FK_building_id_STUDENT FOREIGN KEY (building_id) REFERENCES BUILDING(building_id)
);

CREATE TABLE machine_type
(type_id CHAR (1) PRIMARY KEY,
type_name CHAR (6) NOT NULL
);

CREATE TABLE machine
(machine_id CHAR(3) PRIMARY KEY,
building_id CHAR(2) NOT NULL,
machine_type CHAR(1) NOT NULL,
installation_date DATE NOT NULL,
CONSTRAINT FK_building_id_MACHINE FOREIGN KEY (building_id) REFERENCES BUILDING(building_id),
CONSTRAINT FK_machine_type_MACHINE FOREIGN KEY (machine_type) REFERENCES MACHINE_TYPE(type_id)
);

CREATE TABLE reservation
(reservation_id CHAR(5) PRIMARY KEY,
time_id CHAR(5) NOT NULL,
student_id CHAR(9) NOT NULL,
machine_id CHAR(3) NOT NULL,
start_time TIME NOT NULL,
end_time TIME NOT NULL,
reservation_date DATE NOT NULL,
reservation_status CHAR(9) NOT NULL,
CONSTRAINT FK_student_id_RESERVATION FOREIGN KEY (student_id) REFERENCES STUDENT(student_id),
CONSTRAINT FK_machine_id_RESERVATION FOREIGN KEY (machine_id) REFERENCES MACHINE(machine_id)
);

CREATE TABLE maintenance_request
(request_id CHAR(4) PRIMARY KEY,
machine_id CHAR(3) NOT NULL,
machine_type CHAR(1) NOT NULL,
request_date DATE NOT NULL,
issue_description CHAR(50) NOT NULL,
repair_date DATE,
repair_description CHAR(50),
repair_status CHAR(20),
CONSTRAINT FK_machine_id_MAINTENANCE FOREIGN KEY (machine_id) REFERENCES MACHINE(machine_id),
CONSTRAINT FK_machine_type_id_MAINTENANCE FOREIGN KEY (machine_type) REFERENCES MACHINE_TYPE(type_id)
);

